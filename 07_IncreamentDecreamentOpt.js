

// index++ ==> index = index + 1;
// index-- ==> index = index - 1;

console.log(`----------------- Post Increment -----------------`);
var index =10;
var y = index++;
console.log(`index = ${index} and y = ${y}`);

var index =10;
var y = index + 1;
console.log(`index = ${index} and y = ${y}`);

console.log(`----------------- Post Decrement -----------------`);
var index =10;
var y = index--;
console.log(`index = ${index} and y = ${y}`);

var index =10;
var y = index - 1;
console.log(`index = ${index} and y = ${y}`);

console.log(`----------------- Pre Increment -----------------`);
var index =10;
var y = ++index;
console.log(`index = ${index} and y = ${y}`);

console.log(`----------------- pre Decrement -----------------`);
var index =10;
var y = --index;
console.log(`index = ${index} and y = ${y}`);
